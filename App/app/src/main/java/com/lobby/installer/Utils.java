    /**
     * Execute a shell command
     * @param command Command to execute
     * @return Output of command
     */
    public static String executeCommand(String... command) {
        StringBuilder cmdReturn = new StringBuilder();

        try {
            ProcessBuilder processBuilder = new ProcessBuilder(command);
            /**
             * Executing PHP will cause linker issues in Android < Marshmallow
             * So, ignore errors.
             */
            processBuilder.redirectErrorStream(false);

            Process process = processBuilder.start();

            InputStream inputStream = process.getInputStream();
            int c;
            while ((c = inputStream.read()) != -1) {
                cmdReturn.append((char) c);
            }
            String returnVal = cmdReturn.toString();

            Log.d("Command Output", returnVal);
            return returnVal;
        } catch (IOException e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }
        return null;
    }

    public static String getArch() {
        String arch = System.getProperty("os.arch").substring(0, 3).toLowerCase();
        if (arch.equals("aar") || arch.equals("arm")) {
            return "arm";
        }else if(arch.equals("mip")){
            return "mips";
        }else{
            return "x86";
        }
    }

    /**
     * Replace a string in file
     * @param path Path to file
     * @param find String to replace
     * @param replace The replacement string
     */
    public static void replaceInFile(String path, String find, String replace){
        try {
            String f = getStringFromFile(path);
            f = f.replace(find, replace);

            File outF = new File(path);
            FileOutputStream stream = new FileOutputStream(outF);

            try {
                stream.write(f.getBytes());
            } finally {
                stream.close();
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }

    public static String getStringFromFile(String filePath) throws Exception {
        File fl = new File(filePath);
        FileInputStream fin = new FileInputStream(fl);
        String ret = convertStreamToString(fin);
        //Make sure you close all streams.
        fin.close();
        return ret;
    }

    public static String convertStreamToString(InputStream is) throws Exception {
        BufferedReader reader = new BufferedReader(new InputStreamReader(is));
        StringBuilder sb = new StringBuilder();
        String line = null;
        while ((line = reader.readLine()) != null) {
            sb.append(line).append("\n");
        }
        reader.close();
        return sb.toString();
    }

    public static String humanReadableSize(long bytes, boolean si) {
        int unit = si ? 1000 : 1024;
        if (bytes < unit) return bytes + " B";
        int exp = (int) (Math.log(bytes) / Math.log(unit));
        String pre = (si ? "kMGTPE" : "KMGTPE").charAt(exp-1) + (si ? "" : "i");
        return String.format("%.1f %sB", bytes / Math.pow(unit, exp), pre);
    }

